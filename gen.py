    def pwgen(self):
        y = self.ui.lineEdit.text()
        try:
            y = float(y)
        except ValueError as e:
            QtGui.QMessageBox.about(self, 'Error', 'length must be a number')
            return
        x = ''.join(random.choice(string.ascii_uppercase + string.ascii_lowercase + string.digits) for _ in range(int(y)))
        self.ui.lineEdit_2.setText(x)


